	public static void quit() {
		if (Crane.isInitialized()) {
			Crane.getInstance().resetPosition();
		}
		try {
			Thread.sleep(1000);
		} catch (InterruptedException e) {
			System.err.println("Quit cannot sleep");
		}
		System.exit(0);
	}
	
	public static void error(String errorMessage) {
		lejos.nxt.Sound.buzz();
		System.err.println("ERROR: " + errorMessage);
		lejos.nxt.Button.waitForPress();
		System.exit(0);
	}
	
	//Resets the crane position.
	public static void resetCrane() {
		/*
		 * This check is strange but if the crane is not used before
		 * we don't want to make a "double reset"*.
		 * 
		 * *(double reset) - First time the a crane object request is
		 * made we reset the crane. But if we try to get a crane object
		 * just to make a reset this is very meaningless because we
		 * need to reset the crane before we reset the crane.
		 */
		if (Crane.isInitialized()) {
			Crane.getInstance().resetPosition();
		} else {
			/*
			 * If the crane is never before used we just
			 * make a new crane object (this oberation will
			 * reset the crane automatic).
			 */
			Crane.getInstance();
		}
	}
	
